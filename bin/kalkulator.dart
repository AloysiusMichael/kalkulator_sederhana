class kalkulator {
  double kal1;
  double kal2;
  kalkulator(this.kal1, this.kal2);

  double Tambah(kal1, kal2) {
    return kal1 + kal2;
  }

  double Kurang(kal1, kal2) {
    return kal1 - kal2;
  }

  double Kali(kal1, kal2) {
    return kal1 * kal2;
  }

  double Bagi(kal1, kal2) {
    return kal1 / kal2;
  }

}